public class primit {
    public static void main(String[] args) {
        
        int x1 = 509;
        int x2 = 507;
        x2 = 510;
        System.out.println("x1: " + x1); // x1 remains unchanged
        System.out.println("x2: " + x2); // x2 is updated to 510
        // Demonstrating primitive data types
    }
}
